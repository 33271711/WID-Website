    <p class="AboutParagraph2" style="padding-left: 60px">Women in Dentistry  Society (WA) is a not-for-profit organisation, which was initially established to provide support for the growing number of female dentists and dental specialists in Western Australia, who were faced with the challenge of balancing multiple roles (i.e. as clinicians, practice owners, employees, mothers, daughters, significant others and individuals) whilst trying to maintain health and well-being in both their professional and personal lives. We have since expanded our society to provide support not only of dentistry but the wider community as well. <br><br> In terms of supporting dentistry, we aim to assist all dental professionals in achieving clinical excellence, improving business management and deriving personal satisfaction from career and family lives. We strongly believe that regular social and educational events aid in promoting continuing professional development and nurturing genuine friendships. Women in Dentistry Society (WA) has initiated a mentoring program, Cup of Coffee, to facilitate this. <br><br> In support of the wider community, part of the profits from our events are donated to various charities and organisations, including dental scholarships and research. Reaching out on a personal level to the vulnerable in the community has culminated in the launch of the Healing Smiles program, a clinical volunteering program that provides pro-bono dental care and rehabilitation to survivors of domestic violence.</p></div>
